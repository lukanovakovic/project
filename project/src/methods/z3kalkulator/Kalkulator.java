package methods.z3kalkulator;

public class Kalkulator {

	public int saberi(int a, int b) {
		return a + b;
	}

	public int oduzmi(int a, int b) {
		return a - b;
	}

	public int pomnozi(int a, int b) {

		return a * b;
	}

	public double pomnozi(double a, double b) {

		return a * b;
	}

	public double podeliRealne(double a, double b) {

		return a / b;
	}

	public int podeliDIV(int a, int b) {

		return a / b;
	}

	public int podeliMOD(int a, int b) {

		return a % b;
	}
}
